package EX_10_For_Loop;

public class Lab01_For_Loop {
    public static void main(String[] args) {
        // For Loop
        // Help to repeat Code
        // Initialization -> Condition-> Updateion (Increment/decrement
        for (int i=0; i<10; i++)
        {
            System.out.println(i);
        }
    }
}
