package EX_10_For_Loop;

public class Lab11_For_Loop {
    public static void main(String[] args) {
        for (int i = 1; i <=50 ; i++) {
            if (i%2 == 0) {
                System.out.println("even Number"+ i);

            }else
            {
                System.out.println("Odd Number"+ i);
            }

        }
    }
}
