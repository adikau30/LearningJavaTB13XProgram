package EX_10_For_Loop;

public class Lab10_For_Loop {
    public static void main(String[] args) {
        for (int i=0;i<10;i++)// 0 to 9-. 10 times
        {
            System.out.println(i);
        }
        for (int a=1;a<=10;a++) //1 to 10 -> 10 times
        {
            System.out.println(a);
        }
    }
}
