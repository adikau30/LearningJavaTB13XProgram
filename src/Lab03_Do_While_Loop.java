public class Lab03_Do_While_Loop {
    public static void main(String[] args) {
        // web automation
        int number = 0;
        // 0-> it can be 0 to 10
        do {
            System.out.println(number);
            // call functional number
            // close the excel file or open file
            number++;
        }while (number<0);
    }
}
