package EX_07_Increment_Decrement_OP;

public class Lab_In_De_OP_advance_IP_02 {
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a + ++a);
        System.out.println(a);
        // ++a = 11
        // now a= 11
        // ++a= 12
        // Now a= 12
        // 11+ 12= 23
        // a= 12

    }
}
