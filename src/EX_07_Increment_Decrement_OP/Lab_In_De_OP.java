package EX_07_Increment_Decrement_OP;

public class Lab_In_De_OP {
    public static void main(String[] args) {
        int a = 10;
        int b= ++a;
        System.out.println(a);
        System.out.println(b);
        // line 7 /a= 10/ b= NA
        //LINE 8 / a= pre increment so 11/b=11
    }
}
