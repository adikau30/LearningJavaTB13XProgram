package Ex_15_Stringbuffer_stringbuilder_string_function;

public class Lab01_Stringbuffer_stringbuilder_string_function {
    public static void main(String[] args) {
        String s= "aditya";
        String s1= new String("aditya");

        StringBuffer s2= new StringBuffer("aditya");
        StringBuilder s3= new StringBuilder("aditya");

        System.out.println(s);
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
